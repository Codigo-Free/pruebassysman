-- CLIENTE: aggregate root independiente, referenciado por ORDEN mediante ID_CLIENTE.
CREATE TABLE CLIENTE (
    ID_CLIENTE          NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE              VARCHAR2(200) NOT NULL,
    DOCUMENTO           VARCHAR2(50) NOT NULL,
    EMAIL               VARCHAR2(150),
    TELEFONO            VARCHAR2(30),
    FECHA_CREACION      TIMESTAMP WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    FECHA_MODIFICACION  TIMESTAMP WITH LOCAL TIME ZONE,
    USUARIO_CREA        VARCHAR2(100),
    USUARIO_MODIFICA    VARCHAR2(100),
    CONSTRAINT PK_CLIENTE PRIMARY KEY (ID_CLIENTE),
    CONSTRAINT UQ_CLIENTE_DOCUMENTO UNIQUE (DOCUMENTO)
);

-- ORDEN: particionada por rango mensual sobre FECHA_CREACION para soportar 1M órdenes/mes.
-- VERSION habilita optimistic locking (@Version en JPA) coordinado con el control de
-- versión que también valida SP_ACTUALIZAR_ESTADO_ORDEN de forma atómica.
CREATE TABLE ORDEN (
    ID_ORDEN            NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_CLIENTE          NUMBER NOT NULL,
    TIPO_ORDEN          VARCHAR2(30) NOT NULL,
    ESTADO              VARCHAR2(30) NOT NULL,
    DESCRIPCION         VARCHAR2(1000),
    DIRECCION_SERVICIO  VARCHAR2(300),
    FECHA_CREACION      TIMESTAMP WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    FECHA_MODIFICACION  TIMESTAMP WITH LOCAL TIME ZONE,
    USUARIO_CREA        VARCHAR2(100),
    USUARIO_MODIFICA    VARCHAR2(100),
    VERSION             NUMBER DEFAULT 0 NOT NULL,
    CONSTRAINT PK_ORDEN PRIMARY KEY (ID_ORDEN),
    CONSTRAINT FK_ORDEN_CLIENTE FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE (ID_CLIENTE),
    CONSTRAINT CK_ORDEN_ESTADO CHECK (ESTADO IN ('CREADA', 'ASIGNADA', 'EN_PROCESO', 'COMPLETADA', 'CANCELADA')),
    CONSTRAINT CK_ORDEN_TIPO CHECK (TIPO_ORDEN IN
        ('INSTALACION', 'MANTENIMIENTO', 'REPARACION', 'CORTE', 'RECONEXION', 'INSPECCION'))
)
PARTITION BY RANGE (FECHA_CREACION)
INTERVAL (NUMTOYMINTERVAL(1, 'MONTH'))
(
    PARTITION P_INICIAL VALUES LESS THAN (TIMESTAMP '2026-01-01 00:00:00 +00:00')
);

-- ORDEN_HISTORICO: registro de auditoría, insert-only, poblado exclusivamente por
-- SP_ACTUALIZAR_ESTADO_ORDEN (ver V4) dentro de la misma transacción que actualiza ORDEN.
CREATE TABLE ORDEN_HISTORICO (
    ID_HISTORICO         NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_ORDEN             NUMBER NOT NULL,
    ESTADO_ANTERIOR      VARCHAR2(30),
    ESTADO_NUEVO         VARCHAR2(30) NOT NULL,
    USUARIO_MODIFICA     VARCHAR2(100) NOT NULL,
    FECHA_MODIFICACION   TIMESTAMP WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    OBSERVACION          VARCHAR2(500),
    CONSTRAINT PK_ORDEN_HISTORICO PRIMARY KEY (ID_HISTORICO),
    CONSTRAINT FK_HIST_ORDEN FOREIGN KEY (ID_ORDEN) REFERENCES ORDEN (ID_ORDEN)
)
PARTITION BY RANGE (FECHA_MODIFICACION)
INTERVAL (NUMTOYMINTERVAL(1, 'MONTH'))
(
    PARTITION P_INICIAL VALUES LESS THAN (TIMESTAMP '2026-01-01 00:00:00 +00:00')
);
